#!/bin/bash

# Definição de cores
AZUL='\033[0;34m'
VERDE='\033[0;32m'
CIANO='\033[0;36m'
AMARELO='\033[1;33m'
RESET='\033[0m'

cabecalho() {
    clear
    echo -e "${CIANO}=========================================================="
    echo -e "${AMARELO}       ___ ____   ___    _____           _ _    _ _ "
    echo -e "      |_ _/ ___| / _ \  |_   _|__   ___ | | | _(_) |"
    echo -e "       | |\___ \| | | |   | |/ _ \ / _ \| | |/ / | |"
    echo -e "       | | ___) | |_| |   | | (_) | (_) | |   <| | |"
    echo -e "      |___|____/ \___/    |_|\___/ \___/|_|_|\_\_|_|"
    echo -e "${CIANO}=========================================================="
    echo -e "${VERDE}  >>> Módulo: $1${RESET}"
    echo -e "${CIANO}----------------------------------------------------------${RESET}"
    echo ""
}


